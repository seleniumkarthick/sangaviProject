package com.test.java.basics;

public class LoopingStatement {
	
	public static void main(String[] args) {
		
		/*
		 * 
		 * for
		 * while
		 * do...while
		 * 
		 */
		
		for(int i=1;i<10;i++){
			System.out.println(i);
		}
		
		int x=10;
		while(x==10){
			System.out.println("while loop");
			x++;
		}
		
		int y=101;
		do{
			System.out.println("do while");
		}while(y==13);
		
		
		
		
		
		
		
		
	}

}
